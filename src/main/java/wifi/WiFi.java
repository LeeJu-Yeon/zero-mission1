package wifi;

public class WiFi {
	
    private String wifiID;               // 와이파이관리번호
    private String gu;                   // 자치구
    private String wifiName;             // 와이파이명
    private String roadAddress;          // 도로명주소
    private String detailAddress;        // 상세주소
    private String installFloor;         // 설치위치(층)
    private String installType;          // 설치유형
    private String installAgency;        // 설치기관
    private String serviceType;          // 서비스구분
    private String networkType;          // 망종류
    private String installYear;          // 설치년도
    private String indoorOutdoor;        // 실내외구분
    private String connectionDetail;     // wifi접속환경
    private String latitude;             // 위도
    private String longitude;            // 경도
    private String workDate;             // 작업일자
    private String distance;             // 입력 좌표와의 거리
    
	public String getWifiID() {
		return wifiID;
	}
	
	public void setWifiID(String wifiID) {
		this.wifiID = wifiID;
	}
	
	public String getGu() {
		return gu;
	}
	
	public void setGu(String gu) {
		this.gu = gu;
	}
	
	public String getWifiName() {
		return wifiName;
	}
	
	public void setWifiName(String wifiName) {
		this.wifiName = wifiName;
	}
	
	public String getRoadAddress() {
		return roadAddress;
	}
	
	public void setRoadAddress(String roadAddress) {
		this.roadAddress = roadAddress;
	}
	
	public String getDetailAddress() {
		return detailAddress;
	}
	
	public void setDetailAddress(String detailAddress) {
		this.detailAddress = detailAddress;
	}
	
	public String getInstallFloor() {
		return installFloor;
	}
	
	public void setInstallFloor(String installFloor) {
		this.installFloor = installFloor;
	}
	
	public String getInstallType() {
		return installType;
	}
	
	public void setInstallType(String installType) {
		this.installType = installType;
	}
	
	public String getInstallAgency() {
		return installAgency;
	}
	
	public void setInstallAgency(String installAgency) {
		this.installAgency = installAgency;
	}
	
	public String getServiceType() {
		return serviceType;
	}
	
	public void setServiceType(String serviceType) {
		this.serviceType = serviceType;
	}
	
	public String getNetworkType() {
		return networkType;
	}
	
	public void setNetworkType(String networkType) {
		this.networkType = networkType;
	}
	
	public String getInstallYear() {
		return installYear;
	}
	
	public void setInstallYear(String installYear) {
		this.installYear = installYear;
	}
	
	public String getIndoorOutdoor() {
		return indoorOutdoor;
	}
	
	public void setIndoorOutdoor(String indoorOutdoor) {
		this.indoorOutdoor = indoorOutdoor;
	}
	
	public String getConnectionDetail() {
		return connectionDetail;
	}
	
	public void setConnectionDetail(String connectionDetail) {
		this.connectionDetail = connectionDetail;
	}
	
	public String getLatitude() {
		return latitude;
	}
	
	public void setLatitude(String latitude) {
		this.latitude = latitude;
	}
	
	public String getLongitude() {
		return longitude;
	}
	
	public void setLongitude(String longitude) {
		this.longitude = longitude;
	}
	
	public String getWorkDate() {
		return workDate;
	}
	
	public void setWorkDate(String workDate) {
		this.workDate = workDate;
	}
	
	public String getDistance() {
		return distance;
	}
	
	public void setDistance(String distance) {
		this.distance = distance;
	}

}
